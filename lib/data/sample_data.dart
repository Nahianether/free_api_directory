  static List<ApiItem> getApisByCategory(String categoryId) {
    return apis.where((api) => api.category == categoryId).toList();
  }

  static List<ApiItem> searchApis(String query) {
    return apis.where((api) => 
      api.name.toLowerCase().contains(query.toLowerCase()) ||
      api.description.toLowerCase().contains(query.toLowerCase()) ||
      api.features.any((feature) => feature.toLowerCase().contains(query.toLowerCase()))
    ).toList();
  }
